		else if(contact_mobile.length !== 10){
			$("#contact_mobile").addClass('invalid');
			$("#contact_mobile").focus();
			return false;
		}
		else if(contact_message == ""){
			$("#contact_message").addClass('invalid');
			$("#contact_message").focus();
			return false;
		}
		else{
			$("#sendmessage").hide();
			$("span.ajax-loader").addClass('is-active');
			$(".wpcf7-response-output").html("");
			$.ajax({
				url: "http://localhost/yfc/enquriymail.php",
				type: "POST",
				data: "contact_name="+contact_name+"&contact_email="+contact_email+"&contact_mobile="+contact_mobile+"&contact_message="+contact_message+"&validates="+validates,
				success: function (response){
